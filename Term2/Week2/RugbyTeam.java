package Week2;

public class RugbyTeam {
    private String teamName;
    private int won;
    private int lost;
    private int drawn;
    private int scored;
    private int against;

    public RugbyTeam(String teamName, int won, int lost, int drawn, int scored, int against) {
        this.teamName = teamName;
        this.won = won;
        this.lost = lost;
        this.drawn = drawn;
        this.scored = scored;
        this.against = against;
    }



}
